USE my_smartkedexwebservices;

DROP TABLE IF EXISTS Catches;
DROP TABLE IF EXISTS Settings;
/*DROP TABLE IF EXISTS Pokemon;*/

CREATE TABLE IF NOT EXISTS Settings (
	Username VARCHAR(20) PRIMARY KEY,
	Password VARCHAR(20),
	Owner VARCHAR(20),
    SmartkedexName VARCHAR(20),
    PokemonGO INT(1)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS Pokemon (
	ID INT (3) PRIMARY KEY,
	PokemonName VARCHAR(20)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS Type (
	TypeName VARCHAR(10) PRIMARY KEY
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS Ulti (
	UltiName VARCHAR(15) PRIMARY KEY,
	DamageDealt INT(3),
	Duration FLOAT(3,2),
	CriticalChance INT(2),
	TypeName VARCHAR(10),
	FOREIGN KEY (TypeName) REFERENCES Type (TypeName)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS Attack (
	AttackName VARCHAR(15) PRIMARY KEY,
	DamageDealt INT(3),
	Duration FLOAT(3,2),
	TypeName VARCHAR(10),
	FOREIGN KEY (TypeName) REFERENCES Type (TypeName)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS HasAttack (
	ID INT(3),
	AttackName VARCHAR(15),
	PRIMARY KEY (ID, AttackName),
	FOREIGN KEY (AttackName) REFERENCES Attack (AttackName),
	FOREIGN KEY (ID) REFERENCES Pokemon (ID)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS HasUlti (
	ID INT(3),
	UltiName VARCHAR(15),
	PRIMARY KEY (ID, UltiName),
	FOREIGN KEY (UltiName) REFERENCES Ulti (UltiName),
	FOREIGN KEY (ID) REFERENCES Pokemon (ID)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS HasType (
	ID INT(3),
	TypeName VARCHAR(10),
	PRIMARY KEY (ID, TypeName),
	FOREIGN KEY (ID) REFERENCES Pokemon (ID),
	FOREIGN KEY (TypeName) REFERENCES Type (TypeName)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS HasStrenght (
	ID INT(3),
	TypeName VARCHAR(10),
	PRIMARY KEY (ID, TypeName),
	FOREIGN KEY (ID) REFERENCES Pokemon (ID),
	FOREIGN KEY (TypeName) REFERENCES Type (TypeName)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS HasWeakness (
	ID INT(3),
	TypeName VARCHAR(10),
	PRIMARY KEY (ID, TypeName),
	FOREIGN KEY (ID) REFERENCES Pokemon (ID),
	FOREIGN KEY (TypeName) REFERENCES Type (TypeName)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS Copy (
	ID INT(3) PRIMARY KEY AUTO_INCREMENT,
	PokemonID INT(3),
	AttackName VARCHAR(15),
	UltiName VARCHAR(15),
	PokemonName VARCHAR(20),
	FOREIGN KEY (PokemonID) REFERENCES Pokemon (ID)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS Catches (
	ID INT(3),
	Username VARCHAR(40),
	PRIMARY KEY (ID, Username),
	FOREIGN KEY (Username) REFERENCES Settings (Username),
	FOREIGN KEY (ID) REFERENCES Copy (ID)
) ENGINE=InnoDB;

